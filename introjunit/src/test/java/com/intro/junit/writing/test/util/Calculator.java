package com.intro.junit.writing.test.util;

public class Calculator {
    public int add(int a, int b) {
        return a + b;
    }

    public int multiply(int a, int b) {
        return a * b;
    }

    public long divide(int a, int b) {
        return (long) a / b;
    }

    public int subtract(int a, int b) {
        return a - b;
    }
}